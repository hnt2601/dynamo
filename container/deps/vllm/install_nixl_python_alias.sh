# Some vLLM base images ship the NIXL Python wheel under a CUDA-suffixed
# distribution name (e.g. `nixl-cu13` -> import name `nixl_cu13`, and
# `nixl-ep-cu13` -> `nixl_ep_cu13`) instead of the canonical `nixl` / `nixl_ep`
# names that vLLM, Dynamo and lmcache import (`from nixl._api import ...`,
# `importlib.import_module("nixl._api")`, `importlib.util.find_spec("nixl")`).
#
# On those bases the canonical import fails at *runtime* with
# `RuntimeError: NIXL is not available`, even though the native libraries and
# the CUDA-suffixed module are present. This installs a thin alias package so
# the canonical name resolves to the already-installed CUDA-suffixed wheel,
# reusing the same compiled extension objects (no duplicate native init, no
# second wheel download that could pull a mismatched CUDA build).
#
# No-op when a real top-level `nixl` package already exists (official
# vllm/vllm-openai base), so it is safe to run unconditionally.
set -euo pipefail

# Resolve site-packages from the active interpreter (honors venv vs system).
SITE_PACKAGES="$(python3 -c 'import sysconfig; print(sysconfig.get_paths()["purelib"])')"

# Write an alias package `${canonical}` that forwards to `${impl}` (its
# CUDA-suffixed sibling), if and only if `${impl}` exists and `${canonical}`
# does not.
write_alias() {
    canonical="$1"
    impl="$2"
    if ! python3 -c "import importlib.util,sys; sys.exit(0 if importlib.util.find_spec('${impl}') else 1)"; then
        return 0  # impl not installed under this name; nothing to alias
    fi
    if python3 -c "import importlib.util,sys; sys.exit(0 if importlib.util.find_spec('${canonical}') else 1)"; then
        echo "install_nixl_python_alias: '${canonical}' already importable; skipping"
        return 0
    fi
    pkg_dir="${SITE_PACKAGES}/${canonical}"
    mkdir -p "${pkg_dir}"
    cat > "${pkg_dir}/__init__.py" <<PY
# Auto-generated by install_nixl_python_alias.sh.
# Alias package: expose ${impl} under the canonical name '${canonical}'.
import sys as _sys
import ${impl} as _impl
from ${impl} import *  # noqa: F401,F403
if hasattr(_impl, "__all__"):
    __all__ = list(_impl.__all__)
# Forward known submodules so 'from ${canonical}._api import ...',
# 'import ${canonical}._bindings' and importlib.import_module('${canonical}._api')
# resolve to the same already-loaded ${impl} submodule objects.
for _sub in ("_api", "_bindings", "_utils", "logging"):
    try:
        _m = __import__("${impl}." + _sub, fromlist=[_sub])
    except Exception:
        continue
    _sys.modules["${canonical}." + _sub] = _m
    globals()[_sub] = _m
PY
    echo "install_nixl_python_alias: created ${canonical} -> ${impl} at ${pkg_dir}"
}

# Detect the CUDA-suffixed NIXL wheels present in this environment and alias
# them to the canonical names. Covers cu12/cu13 (and any future cuNN).
aliased_any=0
for impl in $(python3 - <<'PY'
import pkgutil, re
for m in pkgutil.iter_modules():
    if re.fullmatch(r"nixl_cu\d+", m.name) or re.fullmatch(r"nixl_ep_cu\d+", m.name):
        print(m.name)
PY
); do
    case "${impl}" in
        nixl_cu*)    write_alias "nixl" "${impl}"; aliased_any=1 ;;
        nixl_ep_cu*) write_alias "nixl_ep" "${impl}"; aliased_any=1 ;;
    esac
done

if [ "${aliased_any}" -eq 0 ]; then
    echo "install_nixl_python_alias: no CUDA-suffixed NIXL wheel found; nothing to do"
fi
